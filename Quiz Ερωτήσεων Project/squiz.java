    private void checkAnswer(JButton button) {

        if (button.getText().equals(newGame.getCorrectAnswer())) {
            button.setBackground(Color.GREEN);
            newGame.getPlayingPlayer().increaseAndGetScore();
            keepScore.setText(String.valueOf(newGame.getPlayingPlayer().getScore()));
            refreshPlayer();
        } else {
            button.setBackground(Color.RED);
             refreshPlayer();
        }
        userTurn.setText(newGame.getPlayingPlayer().getName() + " is playing:");
        keepScore.setText(String.valueOf(newGame.getPlayingPlayer().getScore()));
   
        Timer timer = new Timer(800, timerAction);
        timer.start();
        newGame.setIndexOfQuestionInPlay();
        setQuestionAndCandidateAnswers();
        if (newGame.endOfGame()) {
            Squiz.setSelectedIndex(6);
            getTheWinner();
        }
    }

    private void changeButtonColor(Color color) {
        firstButton.setBackground(color);
        secondButton.setBackground(color);
        thirdButton.setBackground(color);
        fourthButton.setBackground(color);
    }
    
    private void refreshPlayer() {
        if (newGame.getQuestionsCounter() == (newGame.getMaxQuestionsPerRound()-1)) {
            Squiz.setSelectedIndex(7);
            p1Label.setText(newGame.getPlayerOne().getName());
            scoreP1Label.setText(String.valueOf(newGame.getPlayerOne().getScore()));
            readyLabel.setText(newGame.getPlayerTwo().getName() + "'s turn!");
            newGame.setPlayingPlayer(newGame.getPlayerTwo());
        }
    }
    
    private void setQuestionAndCandidateAnswers(){
        questionLabel.setText(newGame.getQuestionInPlay().getQuestion());
        firstButton.setText(newGame.getQuestionInPlay().getAnswers()[0]);
        secondButton.setText(newGame.getQuestionInPlay().getAnswers()[1]);
        thirdButton.setText(newGame.getQuestionInPlay().getAnswers()[2]);
        fourthButton.setText(newGame.getQuestionInPlay().getAnswers()[3]);
    }

