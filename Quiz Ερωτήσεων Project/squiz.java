    private void getTheWinner(){
        if (newGame.getPlayerOne().getScore() > newGame.getPlayerTwo().getScore()) {
                resultBox.setText("WE HAVE WINNER!");
                firstUser.setText(String.valueOf(newGame.getPlayerOne().getName()));
                firstScore.setText(String.valueOf(newGame.getPlayerOne().getScore()));
                secondUser.setText(String.valueOf(newGame.getPlayerTwo().getName()));
                secondScore.setText(String.valueOf(newGame.getPlayerTwo().getScore()));
            } else if (newGame.getPlayerOne().getScore() < newGame.getPlayerTwo().getScore()) {
                resultBox.setText("WE HAVE WINNER!");
                firstUser.setText(String.valueOf(newGame.getPlayerTwo().getName()));
                firstScore.setText(String.valueOf(newGame.getPlayerTwo().getScore()));
                secondUser.setText(String.valueOf(newGame.getPlayerOne().getName()));
                secondScore.setText(String.valueOf(newGame.getPlayerOne().getScore()));
            } else {
                resultBox.setText("IT'S A DRAW");
                firstUser.setText(String.valueOf(newGame.getPlayerOne().getName()));
                firstScore.setText(String.valueOf(newGame.getPlayerOne().getScore()));
                secondUser.setText(String.valueOf(newGame.getPlayerTwo().getName()));
                secondScore.setText(String.valueOf(newGame.getPlayerTwo().getScore()));
            }
    }
